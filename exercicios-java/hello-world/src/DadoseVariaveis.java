public class DadoseVariaveis {
    public static void main(String[] args) {

        int idade = 10;
        double preco = 10.0;
        char sexo = 'M';
        boolean temfilhos = true;

        System.out.println(idade);
        System.out.println(preco);
        System.out.println(sexo);
        System.out.println(temfilhos);
    }
}
